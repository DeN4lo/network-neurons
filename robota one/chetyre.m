%% =========================================================
%  ЗАДАЧА 1. КЛАСИФІКАЦІЯ АТЛЕТІВ
%  Ознаки:
%  x1 - зріст, см
%  x2 - вага, кг
%
%  Класи:
%  0 - жокеї
%  1 - баскетболісти
%% =========================================================

% -----------------------------
% Формування навчальної вибірки
% -----------------------------
% Жокеї: зріст <= 170, вага <= 65
jockeys = [...
    150 152 155 158 160 162 165 167 168 169;
     45  48  50  52  54  56  58  60  62  64];

% Баскетболісти: зріст > 170, вага > 65
basketball = [...
    175 178 180 183 185 188 190 193 195 198;
     70  72  75  78  80  84  87  90  94  98];

% Об'єднання вхідних даних
P = [jockeys basketball];

% Цільові виходи
% 0 - жокеї, 1 - баскетболісти
T = [zeros(1, size(jockeys,2)) ones(1, size(basketball,2))];

% -----------------------------
% Нормування вхідних даних
% -----------------------------
% mapminmax нормує дані до діапазону [-1; 1]
[Pn, ps] = mapminmax(P);

% -----------------------------
% Створення персептрона
% -----------------------------
net1 = perceptron;
net1.name = 'Athletes Perceptron';

% Функція навчання для ваг і зміщення
net1.inputWeights{1,1}.learnFcn = 'learnpn';
net1.biases{1}.learnFcn = 'learnpn';

% Кількість епох
net1.trainParam.epochs = 50;

% -----------------------------
% Навчання мережі
% -----------------------------
net1 = train(net1, Pn, T);

% -----------------------------
% Перевірка роботи мережі
% -----------------------------
Y = net1(Pn);

disp('Результати класифікації для навчальної вибірки:');
disp(Y);

disp('Цільові значення:');
disp(T);

% Точність
acc1 = sum(Y == T) / numel(T) * 100;
fprintf('Точність на навчальній вибірці: %.2f %%\n', acc1);

% -----------------------------
% Моделювання нових прикладів
% -----------------------------
testAthletes = [...
    168 172 166 185 178 160;
     60  68  70  88  74  50];

testAthletesN = mapminmax('apply', testAthletes, ps);
testOutput = net1(testAthletesN);

disp('Тестові спортсмени [зріст; вага]:');
disp(testAthletes);

disp('Результат класифікації тестових прикладів:');
disp(testOutput);
disp('0 - жокей, 1 - баскетболіст');

% -----------------------------
% Побудова графіка областей класифікації
% -----------------------------
h = 145:1:205;
w = 40:1:105;
[H, W] = meshgrid(h, w);

gridPoints = [H(:)'; W(:)'];
gridPointsN = mapminmax('apply', gridPoints, ps);

gridOutput = net1(gridPointsN);
classMap = reshape(gridOutput, size(H));

figure;
hold on;
grid on;
box on;

% Фон областей класифікації
contourf(H, W, classMap, 1, 'LineColor', 'none');
alpha(0.35);

% Межа класифікації
contour(H, W, classMap, [0.5 0.5], 'k', 'LineWidth', 2);

% Навчальні точки
idxJ = find(T == 0);
idxB = find(T == 1);

plot(P(1, idxJ), P(2, idxJ), 'bo', ...
    'MarkerSize', 9, 'LineWidth', 2, 'MarkerFaceColor', 'b');

plot(P(1, idxB), P(2, idxB), 'rs', ...
    'MarkerSize', 9, 'LineWidth', 2, 'MarkerFaceColor', 'r');

xlabel('Зріст, см');
ylabel('Вага, кг');
title('Класифікація атлетів персептроном');
legend('Області класифікації', 'Межа поділу', ...
       'Жокеї', 'Баскетболісти', ...
       'Location', 'northwest');
hold off;